        <h3>Ways of Studying</h3>
        <ul>
          <li>Active Learning</li>
          <p>Active learning involves engaging with the material actively rather than passively reading or listening. Some active learning techniques include taking notes, summarizing, creating flashcards, or using mnemonic devices. These techniques help you process and retain information better than just reading or listening passively.</p>
          <li>Group Study</li>
          <p>Studying with a group can be an effective way to learn and retain information. Group study allows you to discuss difficult concepts and work through problems together. It can also be motivating to study with peers who have similar academic goals.</p>
          <li>Practice Tests</li>
          <p>Practice tests are an effective way to prepare for exams. They help you identify areas where you need more practice and familiarize yourself with the exam format. Practice tests can also help reduce exam anxiety.</p>
          <li>Teach Others</li>
           <p>Teaching others is an excellent way to reinforce your own knowledge. When you explain a concept to someone else, you are forced to clarify your understanding of the material. Teaching also helps you identify areas where you need more practice.</p>
