        <h3>Creating a Study Timetable</h3>
        <ol>
          <li>Identify Priorities</li>
          <p>Before creating a study timetable, you need to identify your academic priorities. Determine which subjects require the most attention and set realistic goals for each study session.</p>
          <li>Set a Schedule</li>  
          <p>Once you have identified your priorities, set a schedule for your study sessions. It's important to allocate time for each subject and avoid cramming. Make sure to include breaks in your schedule to avoid burnout.</p>
          <li>Stick to the Schedule</li>
          <p>Creating a study timetable is one thing, but sticking to it is another. It's essential to discipline yourself and avoid distractions during study sessions. Turn off your phone and social media notifications, and let your family and friends know that you will be unavailable during study time.</p>
          <li>Review and Adjust</li>
          <p>It's important to review your study timetable regularly and adjust it as needed. If you find that you are not making progress in a particular subject, adjust your study schedule accordingly.</p>
